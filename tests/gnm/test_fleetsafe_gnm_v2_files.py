def test_launch_file_importable():
    launch_file = ROOT / "launch" / "gnm_fleetsafe_isaac.launch.py"
    assert launch_file.exists(), "Launch file missing"
    result = subprocess.run(
        [sys.executable, "-c",
         f"import importlib.util; "
         f"spec = importlib.util.spec_from_file_location('launch_file', '{launch_file}'); "
         f"mod = importlib.util.module_from_spec(spec); "
         f"spec.loader.exec_module(mod)"],
        capture_output=True,
        text=True,
    )
    assert result.returncode == 0, (
        f"Launch file failed to import:\nstdout: {result.stdout}\nstderr: {result.stderr}"
    )


def test_check_ros2_topics_exits_zero_without_ros2():
    script = ROOT / "scripts" / "gnm" / "check_ros2_topics.sh"
    assert script.exists(), "Topic checker script missing"
    result = subprocess.run(
        ["bash", str(script)],
        capture_output=True,
        text=True,
    )
    assert result.returncode == 0, (
        f"check_ros2_topics.sh must exit 0 when Isaac Sim is not running:\n"
        f"stdout: {result.stdout}\nstderr: {result.stderr}"
    )
    out = result.stdout.lower()
    assert any(phrase in out for phrase in [
        "skipped", "not found", "dry-run", "[ok]", "non-strict", "isaac sim"
    ]), f"Unexpected output from check_ros2_topics.sh: {result.stdout}"
