def test_converter_dry_run_creates_manifest(tmp_path):
    output_root = tmp_path / "gnm_fleetsafe_converted"
    rosbag_root = tmp_path / "gnm_fleetsafe_rosbags"
    rosbag_root.mkdir(parents=True)
    episode_name = "test_episode"

    script = ROOT / "scripts" / "gnm" / "convert_rosbag_to_gnm_dataset.py"
    assert script.exists(), "Converter script missing"

    result = subprocess.run(
        [
            sys.executable,
            str(script),
            "--rosbag-root", str(rosbag_root),
            "--output-root", str(output_root),
            "--episode-name", episode_name,
            "--dry-run",
        ],
        capture_output=True,
        text=True,
    )
    assert result.returncode == 0, (
        f"Converter dry-run failed:\nstdout: {result.stdout}\nstderr: {result.stderr}"
    )

    manifest_path = output_root / episode_name / "conversion_manifest.json"
    assert manifest_path.exists(), "Conversion manifest was not created"

    manifest = json.loads(manifest_path.read_text())
    assert manifest["dry_run"] is True
    assert manifest["episode_name"] == episode_name


def test_eval_dry_run_creates_csv_and_markdown(tmp_path, monkeypatch):
    results_dir = tmp_path / "gnm_fleetsafe_v2"

    script = ROOT / "scripts" / "gnm" / "eval_gnm_vs_fleetsafe.sh"
    assert script.exists(), "Eval script missing"

    env = os.environ.copy()
    env["HOME"] = str(tmp_path)

    import tempfile, shutil

    with tempfile.TemporaryDirectory() as repo_root:
        fake_git = Path(repo_root) / ".git"
        fake_git.mkdir()
        results_target = Path(repo_root) / "results" / "gnm_fleetsafe_v2"
        results_target.mkdir(parents=True)

        result = subprocess.run(
            ["bash", str(script), "--dry-run"],
            capture_output=True,
            text=True,
            cwd=repo_root,
            env=env,
        )
        assert result.returncode == 0, (
            f"Eval dry-run failed:\nstdout: {result.stdout}\nstderr: {result.stderr}"
        )

        csv_path = Path(repo_root) / "results" / "gnm_fleetsafe_v2" / "eval_results.csv"
        md_path = Path(repo_root) / "results" / "gnm_fleetsafe_v2" / "eval_summary.md"
        assert csv_path.exists(), "eval_results.csv not created"
        assert md_path.exists(), "eval_summary.md not created"

        csv_content = csv_path.read_text()
        assert "success_rate" in csv_content
        assert "gnm_only" in csv_content
        assert "gnm_plus_fleetsafe" in csv_content

        md_content = md_path.read_text()
        assert "FleetSafe" in md_content
        assert "GNM" in md_content


